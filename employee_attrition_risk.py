explainer = shap.TreeExplainer(xgb)

def get_top_shap_features(shap_row, feature_names, top_n=3):
    contributions = list(zip(feature_names, shap_row))
    contributions.sort(key=lambda x: abs(x[1]), reverse=True)
    return [(name, val, "increases risk" if val > 0 else "decreases risk") for name, val in contributions[:top_n]]

def predict_attrition(age, monthly_income, overtime, business_travel, job_satisfaction,
                       years_at_company, distance_from_home, num_companies_worked,
                       job_role, department, work_life_balance):

    input_dict = {col: 0 for col in feature_columns}

    input_dict['Age'] = age
    input_dict['MonthlyIncome'] = monthly_income
    input_dict['JobSatisfaction'] = job_satisfaction
    input_dict['YearsAtCompany'] = years_at_company
    input_dict['DistanceFromHome'] = distance_from_home
    input_dict['NumCompaniesWorked'] = num_companies_worked
    input_dict['WorkLifeBalance'] = work_life_balance
    input_dict['IncomePerYearAtCompany'] = monthly_income / (years_at_company + 1)
    input_dict['PromotionGap'] = years_at_company

    if overtime == 'Yes' and 'OverTime_Yes' in input_dict:
        input_dict['OverTime_Yes'] = 1

    travel_col = f'BusinessTravel_{business_travel}'
    if travel_col in input_dict:
        input_dict[travel_col] = 1

    role_col = f'JobRole_{job_role}'
    if role_col in input_dict:
        input_dict[role_col] = 1

    dept_col = f'Department_{department}'
    if dept_col in input_dict:
        input_dict[dept_col] = 1

    input_df = pd.DataFrame([input_dict])[feature_columns]
    input_scaled = scaler.transform(input_df)

    risk_prob = xgb.predict_proba(input_scaled)[0][1]

    shap_vals = explainer.shap_values(input_df)
    top_feats = get_top_shap_features(shap_vals[0], feature_columns, top_n=3)
    reasons = ", ".join([f"{f[0].replace('_', ' ')} ({f[2]})" for f in top_feats])

    return f"Attrition Risk: {risk_prob*100:.1f}%\nTop factors: {reasons}"

